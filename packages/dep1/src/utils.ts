export class Utils {
  public static add(num1: number, num2: number) {
    return num1 + num2
  }

  public static multiply(num1: number, num2: number) {
    return num1 * num2;
  }

  public static divide(num1: number, num2: number) {
    return num1 / num2;
  }

  public static subtract(num1: number, num2: number) {
    return num1 - num2;
  }
}
